namespace Calculators
{
    internal class Arithmetic
    {
        internal float Add(float x, float y) { return x + y; }
        internal float Sub(float x, float y) { return x - y; }
        internal float Mul(float x, float y) { return x * y; }
        internal float Div(float x, float y) { return x / y; }
    }
}
